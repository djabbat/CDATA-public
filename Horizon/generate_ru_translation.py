def set_line_spacing(paragraph, value=1.15):
    pPr = paragraph._p.get_or_add_pPr()
    spEl = OxmlElement('w:spacing')
    spEl.set(qn('w:line'), str(int(value * 240)))
    spEl.set(qn('w:lineRule'), 'auto')
