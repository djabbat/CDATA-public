//! Internationalization (i18n) for Cell DT GUI
//! Supports 7 languages: EN, FR, ES, RU, ZH, AR, KA (Georgian)

use serde::{Deserialize, Serialize};

/// Supported display languages
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize, Default)]
pub enum Lang {
    #[default]
    En,
    Fr,
    Es,
    Ru,
    Zh,
    Ar,
    Ka,
}

impl Lang {
    pub fn display_name(self) -> &'static str {
        match self {
            Lang::En => "🇬🇧 English",
            Lang::Fr => "🇫🇷 Français",
            Lang::Es => "🇪🇸 Español",
            Lang::Ru => "🇷🇺 Русский",
            Lang::Zh => "🇨🇳 中文",
            Lang::Ar => "🌙 العربية",
            Lang::Ka => "🇬🇪 ქართული",
        }
    }

    pub fn all() -> &'static [Lang] {
        &[Lang::En, Lang::Fr, Lang::Es, Lang::Ru, Lang::Zh, Lang::Ar, Lang::Ka]
    }

    /// Returns the translation table for this language
    pub fn tr(self) -> &'static Tr {
        match self {
            Lang::En => &EN,
            Lang::Fr => &FR,
            Lang::Es => &ES,
            Lang::Ru => &RU,
            Lang::Zh => &ZH,
            Lang::Ar => &AR,
            Lang::Ka => &KA,
        }
    }
}

